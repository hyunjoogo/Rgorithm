# Breadth-First Search
# The algorithm starts with a root node and visit the node itself first.
# root노드를 스스로 방문 리스트에 넣고 시작한다.
# Then traverse its neighbors, traverse its second level neighbors, 
# traverse its third level neighbors, 
# so on and so forth.
# 루트 노드의 이웃을 방문하고 그 이웃의 이웃을 방문하고 (왼쪽부터)

# 핵심키워드는
# 자신을 방문 리스트에 넣으면 큐에 자신의 이웃들을 왼쪽부터 넣는다

